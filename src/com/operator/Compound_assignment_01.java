package com.operator;

public class Compound_assignment_01 {
	
	public static void main(String[] args) {
		int a=10;
		int b=10;
		int c=10;
		int d=10;
		int e=22;
		
		System.out.println(a+=2); //12
		System.out.println(b-=2); //8
		System.out.println(c*=2); //20
		System.out.println(d/=2); //5
		System.out.println(e%=10); //0
	}
}
