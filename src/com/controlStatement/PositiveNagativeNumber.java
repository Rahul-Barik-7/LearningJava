public class PositiveNagativeNumber {

	public static void main(String[] args) {
			
		int num=10;
		if(num>0)
		{
			System.out.println("Positive Number...!");
		}
		else if(num<0)
		{
			System.out.println("Nagative Number...!");
		}
		else
		{
			System.out.println("Zero...!");
		}
	}

}
